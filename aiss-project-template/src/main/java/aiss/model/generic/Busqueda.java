package aiss.model.generic;

public class Busqueda {


	String destino;


	public Busqueda() {

		this.destino = "";

	}

	public Busqueda(String destino) {

		this.destino = destino;


	}



	public String getDestino() {
		return destino;
	}

	public void setDestino(String destino) {
		this.destino = destino;
	}



}
